package day0130.operator.sample;

public class OperEx11 {
	public static void main(String[] args) {
		
		int a = 10;
		int b = 4;
		
		System.out.println(a + b);
		System.out.println(a);
		System.out.println(b);
		
		System.out.println(a += b);
		System.out.println(a);
		System.out.println(a -= b);
		System.out.println(a);
		System.out.println(a *= b);
		System.out.println(a);
		System.out.println(a /= b);
		System.out.println(a);
		System.out.println(a %= b);
		System.out.println(a);

	}

}
